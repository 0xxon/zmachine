package de.lilawelt.zmachine.client.storage;

import java.util.List;

import com.google.gwt.user.client.rpc.RemoteService;
import com.google.gwt.user.client.rpc.RemoteServiceRelativePath;

@RemoteServiceRelativePath("StorageService")
public interface StorageService extends RemoteService {
	
	public List<String> getSavedGames(String GameUid) throws StorageException;
	
	public String save(String SaveName, String GameUid, String SaveData) throws StorageException;
	
	public String restore(String SaveName, String GameUid) throws StorageException;

}
